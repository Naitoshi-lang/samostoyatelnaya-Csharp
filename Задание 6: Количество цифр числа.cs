using System;

public class DigitCounter
{
    // Метод для подсчета количества цифр в числе
    public static int CountDigits(int number)
    {
        // Обработка отрицательных чисел (берем модуль)
        if (number < 0)
        {
            number = Math.Abs(number);
        }

        // Обработка случая, когда число равно 0
        if (number == 0)
        {
            return 1; // Число 0 состоит из одной цифры
        }

        int count = 0; // Инициализируем счетчик цифр

        // Убираем последнюю цифру, пока число не станет 0
        while (number > 0)
        {
            number /= 10; // Целочисленное деление на 10 отбрасывает последнюю цифру
            count++; // Увеличиваем счетчик цифр
        }

        return count; // Возвращаем общее количество цифр
    }
